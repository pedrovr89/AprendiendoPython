#conversiones de tipos de datos

numero = 5
#str convierte a string
cadena = str(numero)
print(cadena)
print(type(cadena))

cadena = "25"
#int convierte a entero
numero = int(cadena)
print(numero)
print(type(numero))
#float convierte a decimal
cadenaDecimal = "25.5"
numeroDecimal = float(cadenaDecimal)
print(numeroDecimal)
print(type(numeroDecimal))