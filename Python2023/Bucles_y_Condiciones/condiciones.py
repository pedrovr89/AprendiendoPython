# Condiciones (if, elif, else)
a = 3
b = 4
c = 2
d = 6

if(a > b):
    print("A es mayor que B")

if(a > c and b < d):
    print("La primera expresión es correcta")
else:
    print("La primera expresión no es correcta")

if (a > b):
    print("A es mayor que B")
elif (a == b):
    print("A es igual que B")
else:
    print("Ninguna expresión anterior es correcta")