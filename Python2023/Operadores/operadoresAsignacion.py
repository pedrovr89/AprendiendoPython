# Operadores de asignaci√≥n (=,+=,-=,*=,/=,**=)
numero = 5
print(numero)
numero = numero + 4
print(numero)
numero = 5
numero += 4 #equivale a numero = numero + 4
print(numero)
#igual con la resta
numero -= 4 #numero = numero -4
print(numero)
#multiplicacion
numero *= 4 #numero = numero * 4
print(numero)
#division
numero /= 4 #numero = numero / 4
print(numero)
#exponente
numero **= 3 #numero = numero ** 3
print(numero)